public class SwitchCaseSemBreak {

  public static void main(String[] args) {
		String plano = "M"; // M / T

		switch (plano) {
			case "T": {
				System.out.println("5Gb Youtube");
			}
			case "M": {
				System.out.println("Whats e Intagram grátis");
			}
			case "B": {
				System.out.println("100 minutos de ligação");
			}
		}
	}

  
  /*Nesse código, a utilização do switch case como condicional, favorece a simplicidade do código
   * e contribui para as estruturas do código limpo
   */
}
