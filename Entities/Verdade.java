package Entities;

public class Verdade {

  private String verdade;

  public Verdade(String verdade){
    this.verdade = verdade;
  }

  public void setVerdade(String verdade){
    this.verdade = verdade;
  }

  public String getVerdade(){
    return verdade;
  }
  public String toString(){
    return verdade;
  }
}
